package demo;

public class ForDemo {
    public static void main(String[] args) {
        for (int count = 1; count <= 10; count++) {
            System.out.println("Counting " + count);
        }
        System.out.println("---------------------");
        for (int i = 10; i > 0 ; i--) {
            System.out.println("Countdown " + i);
            
        }
        }
    }

